/************************
 * Primitive Types : immutable values
    hover over variable name to see dynamic value type
    string, number, & boolean literals
 ************************
*/
let role = 'developer'; //string
console.log(typeof role);

let salary = 50000; //number
console.log(typeof salary);

let isAdmin = false; //boolean
console.log(typeof isAdmin);

let selection = null; //reset, non-selection is null
console.log(typeof selection);

let firstName; //uninitialized === undefined
console.log(typeof firstName);
